function mostrar(num){
    
    if(num ==0){
        document.getElementById('peso').style.visibility='visible';
        document.getElementById('peso').style.transitionDelay="1s"; 
    }if (num==1) {
        document.getElementById('peso').style.visibility='visible'; 
        document.getElementById('peso').style.transitionDelay="1s"; 
    }if (num==2) {
        document.getElementById('peso').style.visibility='visible';
        document.getElementById('peso').style.transitionDelay="1s";  
    } if(num ==3){
        document.getElementById('peso').style.visibility='hidden';
        document.getElementById('peso').style.transitionDelay="1s";
    }
}




